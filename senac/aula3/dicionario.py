# Resumo sobre Dicionários em Python
# Um dicionário em Python é uma estrutura de dados que armazena pares de chave: valor, 
# permitindo o acesso eficiente aos valores associados a cada chave. Ele é útil quando precisamos armazenar e acessar dados de forma rápida e organizada.

# Características dos Dicionários:
# Chaves únicas: Cada chave em um dicionário deve ser única.
# Valores mutáveis: Os valores podem ser de qualquer tipo e podem ser alterados.

# Conclusão
# Os dicionários são ideais para associar dados de maneira estruturada e eficiente. 
# Com operações rápidas de busca e inserção, eles são uma ferramenta poderosa para organizar e processar informações.